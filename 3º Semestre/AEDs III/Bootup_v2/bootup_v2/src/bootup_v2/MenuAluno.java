    private void buttonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAlterarActionPerformed
        try {
            // Lendo codigo de aluno a ser alterado.
            String opcao = JOptionPane.showInputDialog(this, "Código", "Alteração", JOptionPane.INFORMATION_MESSAGE);

            if (opcao != null) {
                int codigo = Integer.parseInt(opcao);
                // Aluno a ser alterado.
                Aluno alunoAlterado = (Aluno) ARQUIVO.buscarCodigo(codigo);

                if (codigo <= 0) {
                    JOptionPane.showMessageDialog(this, "Código Inválido!", "Alteração", JOptionPane.INFORMATION_MESSAGE);
                } else if (alunoAlterado == null) {
                    JOptionPane.showMessageDialog(this, "Aluno não encontrado!", "Alteração", JOptionPane.ERROR_MESSAGE);
                } else if (JOptionPane.showConfirmDialog(this, alunoAlterado, "Alteração", JOptionPane.OK_CANCEL_OPTION,JOptionPane.INFORMATION_MESSAGE) == 0) {

                    // Fazendo alteração.
                    ControleAluno alteracaoAluno = new ControleAluno(ARQUIVO, alunoAlterado, "Alteração");
                    alteracaoAluno.setLocationRelativeTo(null);
                    alteracaoAluno.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
                    alteracaoAluno.setVisible(true);
                    this.setVisible(false);
                } else {
                    JOptionPane.showMessageDialog(this, "Aluno não alterado! :(", "Alteração", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "ERRO!", "Alteração", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_buttonAlterarActionPerformed

