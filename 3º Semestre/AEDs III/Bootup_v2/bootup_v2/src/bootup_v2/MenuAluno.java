    private void buttonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonExcluirActionPerformed
        String opcao = JOptionPane.showInputDialog(this, "Código", "Alteração", JOptionPane.    INFORMATION_MESSAGE);
        
        if (opcao != null) {
            try {
                // Codigo do aluno a ser excluido.
                int codigo = Integer.parseInt(opcao);
                Aluno alunoExcluir = (Aluno) ARQUIVO.buscarCodigo(codigo);
                
                if (codigo <= 0) {
                    JOptionPane.showMessageDialog(this, "Código Inválido!", "Alteração", JOptionPane.INFORMATION_MESSAGE);
                } else if (alunoExcluir == null) {
                    JOptionPane.showMessageDialog(this, "Aluno não encontrado!", "Exclusão", JOptionPane.ERROR_MESSAGE);
                } else if (JOptionPane.showConfirmDialog(this, alunoExcluir, "Exclusão",JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE) == 0) {
                    
                    ARQUIVO.excluir(codigo);
                    JOptionPane.showMessageDialog(this, "Aluno excluído! :)", "Exclusão", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Aluno não excluído! :(", "Exclusão", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "ERRO!", "Exclusão", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_buttonExcluirActionPerformed

